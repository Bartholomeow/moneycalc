using System;
using System.Collections.Generic;
using System.Text;

namespace MoneyCalc
{
    class Calc
    {
        public Calc() {}
        public double Sum(double a, double b)
        {
            return a + b;

        }

        public double Subtract(double a, double b)
        {
            return a - b;

        }

        public double Multiply(double a, double b)
        {
            return a * b;

        }
        public double Division(double a, double b)
        {
            return a / b;

        }

    }
}
